    private void jButtonConsultaFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultaFuncActionPerformed
        try {

            Connection con= FabricaDeConexao.getConnectionFactory();

            String query = "SELECT * FROM aluno WHERE nomeAluno LIKE ?";
            PreparedStatement cmd;
            cmd = con.prepareStatement(query);
            cmd.setString(1, '%'+jTextFieldPesquisarFunc.getText()+'%');
            ResultSet rs;
            rs = cmd.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTableFunc.getModel();
            model.setNumRows(0);
            while (rs.next()){
                model.addRow(new Object[]{rs.getString("idAluno"),rs.getString("nomeAluno"),rs.getString("rgAluno"),rs.getString("dataNascAluno"),
                    rs.getString("telAluno"),rs.getString("emailAluno"),rs.getString("endAluno"),rs.getString("setorAluno"),rs.getString("dataMatriculaAluno")});
        }
        }
        catch (SQLException ex){
            System.out.println("Ocorreu um erro de SQL"+ex.getMessage());
        }
    }//GEN-LAST:event_jButtonConsultaFuncActionPerformed

